export function getPexelsImage(seed, index = 0) {
  const photoIndex = (seed * 7 + index * 13) % pexelsPhotoIds.length;
  const photoId = pexelsPhotoIds[photoIndex];

  return `https://images.pexels.com/photos/${photoId}/pexels-photo-${photoId}.jpeg?auto=compress&cs=tinysrgb&w=1200`;
}

// Генерує масив унікальних фото для сторінки
export function getUniqueImages(seed, count = 4) {
  const images = [];
  for (let i = 0; i < count; i++) {
    images.push(getPexelsImage(seed, i));
  }
  return images;
}

// Альтернативні фото для різних секцій
export function getSectionImage(seed, section) {
  const sectionSeeds = {
    home: seed,
    about: seed * 3,
    blog: seed * 5,
    contacts: seed * 7,
    resources: seed * 11,
    cases: seed * 13,
    advice: seed * 17,
    faq: seed * 19,
  };

  const sectionSeed = sectionSeeds[section] || seed;
  return getPexelsImage(sectionSeed, 0);
}
